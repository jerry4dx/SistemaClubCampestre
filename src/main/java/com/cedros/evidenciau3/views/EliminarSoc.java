    private void CargarSocios() {
        try {
            SociosInt socioint = new SociosImpl();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            socioint.listar("","").forEach((u) -> model.addRow(new Object[]{u.getFolioSocio(), u.getNombre(), u.getApellidos(), u.getTelefono(), u.getDomicilio(), u.getActividades(), u.getVigencia()}));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

