    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Eliminar Socios:
        String nombre = NomTxt.getText();
        String lastname = ApeTxt.getText();
        
        //Instancia del objeto Socios         
        Socios socio = new Socios();
        socio.setNombre(nombre);
        socio.setApellidos(lastname);
        
        try {
            System.out.print("Entrando al Try");
            SociosInt socioint = new SociosImpl();
            socioint.Eliminar(nombre, lastname);
            
            NomTxt.setText("");
            ApeTxt.setText("");    
        }catch(Exception e){
            System.out.println(e.getMessage());
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurri√≥ un error al tratar de eliminar el registro. \n", "ERROR", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


